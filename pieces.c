uint32_t rand(){

	static uint32_t const a = 1103515245;
	static uint32_t const c = 12345;
	static uint32_t const m = (1<<31);
	static uint32_t Xn = 12343544;

	Xn = (a * Xn + c) % m;
	return Xn;
}

Piece get_rand_piece(void){

	enum Pieces p = rand() % NUM_OF_PIECES;

	Piece piece = {
		.piece = p,
		.rotation = ROTATION_NORMAL,
		.x_start = 0,
		.y_start = 0
	};

	return piece;

}
